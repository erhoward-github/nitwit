<p>
The <a href="http://www.westwindsorarts.org/index.html" target="_blank">West Windsor Arts Council</a> 
and <a href="http://codeforprinceton.org/" target="_blank">Code for Princeton</a> hosted a pilot collaborative project â€“ a data visualization hackathon. 
The all-day event brought together artists, programmers, community members, designers, and more to visualize data in the West Windsor community. 
Through this project, participants explored how data can be visually compelling, informative, and foster civic engagement by creating art based from data. 
</p>
<p>
Over the course of the day, 
teams were led by both developers and artists to create projects in both the physical and digital realm that visualize data in order to bring awareness to the community.
</p>
<hr>
<p>
Out project, led by developer <em>Chris Marsh-Bourdon</em> and artist <em>Laurie Pettine</em>, mined Twitter to understand notions of "safety" that are of concern to the people of West Windsor. 
Topics of interest could be safety in schools, water, food, news/media, elections - all gleaned from analysis of hashtags: the weight of a word all on a Twitter crawl.
</p>
<p>
However, our project, in keeping with the theme of the Hackathon, was much more than an accumulation of data.
Indeed, the abstract data were brought to life with sculpture and various wired boxes and contraptions both amplifying cognition and stimulating artistic appreciation.    
</p>

